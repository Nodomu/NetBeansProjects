
public class Empresa 
{
    int h ;
    int d;

    
    
    public Empresa() 
    {
        h =0 ;
        d =0 ;
    }

    public int getHoras() 
    {
        return h;
    }

    public void setHoras(int horas) 
    {
        h = horas;
    }

    public int getDias() 
    {
        return d;
    }

    public void setDias(int dias) 
    {
        d = dias;
    }
    
    
}
