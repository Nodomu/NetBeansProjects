
package n_articulo;


public class Articulo {

    private 
            int N;
            int A;
    public Articulo()
    {
        N=0;
        A=0;
    }

    public int getN() 
    {
        return N;
    }

    public void setN(int a) 
    {
       N = a;
    }

    public int getA() 
    {
        return A;
    }

    public void setA(int b) 
    {
       A = b;
    }  
    
}
